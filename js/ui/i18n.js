let _lang = 'en';
const _subs = new Set();

export function setLang(id) {
  if (!STRINGS[id]) return;
  if (_lang === id) return;
  _lang = id;
  for (const fn of _subs) { try { fn(); } catch (_) {} }
}

export function t(key) {
  return (STRINGS[_lang] && STRINGS[_lang][key])
      || STRINGS.en[key]
      || key;
}

export function onLangChange(fn) {
  _subs.add(fn);
  return () => _subs.delete(fn);
}

