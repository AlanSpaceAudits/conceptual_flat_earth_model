// Build js/data/shaneEclipses.js from Shane's eclipse catalogue.
//
// Source: scripts/sources/shane_eclipse_catalogue.json — the Dimbleby-pattern
// eclipse catalogue (Eclipse_Master_Workbook). Dates/types/magnitudes/gammas
// are Meeus Ch.54 (proleptic-Gregorian, TT); the cycle framing is Dimbleby's.
// 35,387 eclipses, 3996 BC – AD 3648, solar + lunar.
//
// Output is column-oriented (parallel arrays) to keep the bundle small. The
// runtime schedule builder (js/core/eclipseSchedule.js) expands the columns it
// needs and anchors each eclipse to the sim clock via cycle_jd_tt.
//
//   node scripts/build-shane-eclipses.mjs

import { readFile, writeFile } from 'node:fs/promises';

const SRC = new URL('./sources/shane_eclipse_catalogue.json', import.meta.url);
const OUT = new URL('../js/data/shaneEclipses.js', import.meta.url);

const cat = JSON.parse(await readFile(SRC, 'utf8'));
const col = cat.columns;
const n = cat.rows;

// Confidence strings → small enum so 35k rows don't repeat the long labels.
const confLabels = [];
const confIndex = new Map();
const confOf = (s) => {
  if (!confIndex.has(s)) { confIndex.set(s, confLabels.length); confLabels.push(s); }
  return confIndex.get(s);
};

const jd = [], date = [], lum = [], type = [], mag = [], gamma = [], saros = [], conf = [];
for (let i = 0; i < n; i++) {
  jd.push(col.cycle_jd_tt[i]);
  date.push(col.gregorian_date[i]);
  lum.push(col.luminary[i] === 'Solar' ? 0 : 1);
  type.push(col.type[i]);
  mag.push(col.magnitude[i]);
  gamma.push(col.gamma[i]);
  saros.push(col.saros_series[i]);
  conf.push(confOf(col.confidence[i]));
}

const arr = (a) => '[' + a.join(',') + ']';
const sarr = (a) => '[' + a.map((s) => JSON.stringify(s)).join(',') + ']';

const body = `// Auto-generated by scripts/build-shane-eclipses.mjs — do not edit by hand.
//
// Eclipse catalogue (J. B. Dimbleby cycle pattern). Eclipse dates, types,
// magnitudes and gamma values are standard astronomy (Meeus, Astronomical
// Algorithms 2nd ed., Ch.54; proleptic-Gregorian dates, times TT). The
// team/Line/Anno-Mundi/649-cycle framing is Dimbleby's; the recurrence cycles
// (Saros, Inex, Exeligmos, 649-year date-repeating cycle) are cosmology-
// independent, which is why this catalogue drives a geocentric model.
//
// Prediction is a catalogue lookup, not a per-query ephemeris run: convert a
// date to Julian Day (TT) and binary-search the ascending \`jd\` column.
//
// ${n} eclipses, ${cat.gregorian_range?.join(' … ') || ''} (${lum.filter((x) => x === 0).length} solar / ${lum.filter((x) => x === 1).length} lunar).
// Source workbook: ${cat.source}
//
// Column-oriented parallel arrays, all length N, ordered by ascending jd:
//   jd    — cycle_jd_tt, Julian Day (TT) of greatest eclipse (the sort key)
//   date  — proleptic-Gregorian 'YYYY-MM-DD' (signed year; '-' = BC)
//   lum   — 0 = Solar, 1 = Lunar
//   type  — Total / Annular / Hybrid / Partial / Penumbral (+ '-NC' non-central)
//   mag   — eclipse magnitude
//   gamma — least distance of shadow axis from Earth centre, in Earth-radii
//   saros — Saros series number
//   conf  — index into confLabels (per-row validation provenance)

export const SHANE_ECLIPSES = {
  meta: {
    source: ${JSON.stringify(cat.source)},
    methodology: ${JSON.stringify(cat.methodology)},
    rows: ${n},
    gregorianRange: ${JSON.stringify(cat.gregorian_range || null)},
  },
  confLabels: ${sarr(confLabels)},
  jd: ${arr(jd)},
  date: ${sarr(date)},
  lum: ${arr(lum)},
  type: ${sarr(type)},
  mag: ${arr(mag)},
  gamma: ${arr(gamma)},
  saros: ${arr(saros)},
  conf: ${arr(conf)},
};
`;

await writeFile(OUT, body);
const kb = (Buffer.byteLength(body) / 1024).toFixed(0);
console.log(`wrote js/data/shaneEclipses.js — ${n} eclipses, ${kb} KiB`);
